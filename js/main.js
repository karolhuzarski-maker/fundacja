function renderBioTiles() {
  document.querySelectorAll('[data-bio-tiles]').forEach((container) => {
    container.innerHTML = '';
    bioTiles.forEach((tile) => {
      const card = document.createElement('figure');
      card.className = 'bio-tile';

      const image = document.createElement('img');
      image.src = tile.image;
      image.alt = tile.altPL;
      image.dataset.i18nAttr = `alt:${tile.altTranslationKey}`;
      image.loading = 'lazy';
      image.decoding = 'async';
      image.tabIndex = 0;
      image.setAttribute('role', 'button');
      image.setAttribute('aria-label', 'Otwórz powiększenie zdjęcia');

      card.append(image);
      container.append(card);
    });
  });
}


function initBioLightbox() {
  const gallery = document.querySelector('[data-bio-tiles]');
  if (!gallery) return;

  const lightbox = document.createElement('div');
  lightbox.className = 'bio-lightbox';
  lightbox.setAttribute('role', 'dialog');
  lightbox.setAttribute('aria-modal', 'true');
  lightbox.setAttribute('aria-label', 'Powiększone zdjęcie');
  lightbox.hidden = true;

  const closeButton = document.createElement('button');
  closeButton.className = 'bio-lightbox__close';
  closeButton.type = 'button';
  closeButton.setAttribute('aria-label', 'Zamknij powiększone zdjęcie');
  closeButton.textContent = '×';

  const image = document.createElement('img');
  image.className = 'bio-lightbox__image';
  image.alt = '';

  lightbox.append(closeButton, image);
  document.body.append(lightbox);

  let lastFocusedElement = null;

  function openLightbox(sourceImage) {
    lastFocusedElement = document.activeElement;
    image.src = sourceImage.currentSrc || sourceImage.src;
    image.alt = sourceImage.alt || '';
    lightbox.hidden = false;
    document.body.classList.add('bio-lightbox-open');
    closeButton.focus();
  }

  function closeLightbox() {
    lightbox.hidden = true;
    image.removeAttribute('src');
    document.body.classList.remove('bio-lightbox-open');
    lastFocusedElement?.focus?.();
  }

  gallery.addEventListener('click', (event) => {
    const sourceImage = event.target.closest('.bio-tile img');
    if (!sourceImage || !gallery.contains(sourceImage)) return;
    openLightbox(sourceImage);
  });

  gallery.addEventListener('keydown', (event) => {
    if (event.key !== 'Enter' && event.key !== ' ') return;
    const sourceImage = event.target.closest('.bio-tile img');
    if (!sourceImage || !gallery.contains(sourceImage)) return;
    event.preventDefault();
    openLightbox(sourceImage);
  });

  lightbox.addEventListener('click', (event) => {
    if (event.target === lightbox || event.target === closeButton) closeLightbox();
  });

  image.addEventListener('click', (event) => {
    event.stopPropagation();
  });

  document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape' && !lightbox.hidden) closeLightbox();
  });
}

const LANGUAGE_STORAGE_KEY = 'foc-language';
const supportedLanguages = ['pl', 'en'];
const originalText = new WeakMap();
const originalAttributes = new WeakMap();

function translateValue(value, lang) {
  if (lang === 'pl') return value;
  return translations[lang]?.[value] || value;
}

function translateKey(key, fallback, lang) {
  if (lang === 'pl') return fallback;
  return translations[lang]?.[key] || translations[lang]?.[fallback] || fallback;
}

function setLanguage(lang) {
  const nextLang = supportedLanguages.includes(lang) ? lang : 'pl';
  document.documentElement.lang = nextLang;

  document.querySelectorAll('title, meta[name="description"], meta[property="og:title"], meta[property="og:description"]').forEach((node) => {
    if (node.tagName.toLowerCase() === 'title') {
      if (!originalText.has(node)) originalText.set(node, node.textContent);
      node.textContent = translateValue(originalText.get(node), nextLang);
      return;
    }
    if (!originalAttributes.has(node)) originalAttributes.set(node, { content: node.getAttribute('content') || '' });
    node.setAttribute('content', translateValue(originalAttributes.get(node).content, nextLang));
  });

  document.querySelectorAll('[aria-label]').forEach((node) => {
    if (!originalAttributes.has(node)) originalAttributes.set(node, {});
    const attrs = originalAttributes.get(node);
    if (!('aria-label' in attrs)) attrs['aria-label'] = node.getAttribute('aria-label') || '';
    node.setAttribute('aria-label', translateValue(attrs['aria-label'], nextLang));
  });

  document.querySelectorAll('[data-i18n-attr]').forEach((node) => {
    if (!originalAttributes.has(node)) originalAttributes.set(node, {});
    const attrs = originalAttributes.get(node);

    node.dataset.i18nAttr.split(',').forEach((definition) => {
      const [attribute, key] = definition.split(':').map((part) => part?.trim());
      if (!attribute || !key) return;
      if (!(attribute in attrs)) attrs[attribute] = node.getAttribute(attribute) || '';
      node.setAttribute(attribute, translateKey(key, attrs[attribute], nextLang));
    });
  });

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    if (!originalText.has(node)) originalText.set(node, node.textContent);
    node.textContent = translateKey(node.dataset.i18n, originalText.get(node), nextLang);
  });

  document.querySelectorAll('img[alt]').forEach((node) => {
    if (!node.getAttribute('alt')) return;
    if (!originalAttributes.has(node)) originalAttributes.set(node, {});
    const attrs = originalAttributes.get(node);
    if (!('alt' in attrs)) attrs.alt = node.getAttribute('alt') || '';
    node.setAttribute('alt', translateValue(attrs.alt, nextLang));
  });

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      if (node.parentElement?.closest('script, style, .language-switcher, [data-i18n]')) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    },
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    if (!originalText.has(node)) originalText.set(node, node.nodeValue);
    const source = originalText.get(node);
    const leading = source.match(/^\s*/)[0];
    const trailing = source.match(/\s*$/)[0];
    const core = source.trim();
    node.nodeValue = core ? `${leading}${translateValue(core, nextLang)}${trailing}` : source;
  });

  document.querySelectorAll('.language-option[data-lang]').forEach((button) => {
    const isActive = button.dataset.lang === nextLang;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });
  localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLang);
}

function initLanguageSwitcher() {
  document.querySelectorAll('.language-option[data-lang]').forEach((button) => {
    button.addEventListener('click', () => setLanguage(button.dataset.lang));
  });
  setLanguage(localStorage.getItem(LANGUAGE_STORAGE_KEY) || 'pl');
}

